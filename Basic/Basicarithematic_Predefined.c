#include <stdio.h>
int main()
{
    // Integer datatype
    int a = 10;
    int b = 5;
    // Float datatype
    float c = 22.4;
    float d = 11.2;
    // For Integer operations:-
    printf("Addition of Both Integers= %d \n", a + b);
    printf("Subtraction of Both Integers= %d \n", a - b);
    printf("Multiplication of Both Integers= %d \n", a * b);
    printf("Division of Both Integers= %d \n", a / b);
    // For Float operations:-
    printf("Addition of Both Decimal Values= %.2f \n", c + d);
    printf("Subtraction of Both Decimal Values= %.2f \n", c - d);
    printf("Multipication of Both Decimal Values= %.2f \n", c * d);
    printf("Division of Both Decimal Values= %.2f \n", c / d);
    return 0;
}
